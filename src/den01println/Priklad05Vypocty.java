package den01println;

public class Priklad05Vypocty {

	public static void main(String[] args) {
		int a = 555; 
		int b = 333;
		
		a = 222;
		
		// V príkaze System.out.println môžeme robiť aj rôzne matematické operácie
		System.out.println( a + " x " + b + " = " + (a * b));
		System.out.println( a + " + " + b + " = " + (a + b));
		System.out.println( a + " - " + b + " = " + (a - b) );
		System.out.println( a + " / " + b + " = " + (a / b));
		
	}
}
